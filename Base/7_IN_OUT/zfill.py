print ('\'12\'.zfill(5) =', '12'.zfill(5))    #5意思是总共5位， 不够5位前面补0
print ('\'-3.14\'.zfill(7)=', '-3.14'.zfill(7))  #zfill 也可以正确识别负号
print ('\'3.14159265359\'.zfill(5)=', '3.14159265359'.zfill(5))     #已经超过5位， 无需填充
print ('\'3.14159265359\'.zfill(20)=', '3.14159265359'.zfill(20))
